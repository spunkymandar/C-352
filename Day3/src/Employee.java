
public class Employee {
	// attribute
	private int empId;// identity

	private String empName;
	private double empSalary;

	
	public Employee(int empId, String empName, double empSalary) {
		super();
		this.empId = empId;
		this.empName = empName;
		this.empSalary = empSalary;
	}

	
}
